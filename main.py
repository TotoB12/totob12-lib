def multiply(number, muiltiplier):
    """
    Multiply a given number by a given multiplier.
    
    :param number: The number to multiply.
    :type number: int
    
    :param muiltiplier: The multiplier.
    :type muiltiplier: int
    """
    
    return number * muiltiplier

# Call the function
print(multiply(5, 2))
